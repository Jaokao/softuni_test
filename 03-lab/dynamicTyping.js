let myVariable = 42; // myVariable je tipa number
console.log(typeof myVariable); // `number`

myVariable = `Hello`; // sada je tipa string
console.log(typeof myVariable); // `string`

myVariable = true; // ovde ce biti boolean tipa
console.log(typeof myVariable) // `boolean`

myVariable = [1, 2, 3] // myVariable je sad objekat (niz)
console.log(typeof myVariable) // object